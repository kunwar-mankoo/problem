package UI;

public class Student {
	int SID;
	String Name;
	String RollNo;
	String Email;
	String MobileNo;
	public int getSID() {
		return SID;
	}
	public String getName() {
		return Name;
	}
	public String getRollNo() {
		return RollNo;
	}
	public String getEmail() {
		return Email;
	}
	public String getMobileNo() {
		return MobileNo;
	}

}
